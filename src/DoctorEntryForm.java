    private void cmd_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmd_saveActionPerformed
      try{
          
          
          String info="Insert into DoctorInfo(d_id,d_name,d_sex,d_dept) values(?,?,?,?)";
          pst=conn.prepareStatement(info);
          
          
          
         if((idField.getText() != null && !idField.getText().isEmpty())&& (nameField.getText() != null && !nameField.getText().isEmpty())) 
         { 
             pst.setString(1,idField.getText());
          pst.setString(2,nameField.getText());
          String gender=combo_sex.getSelectedItem().toString();
          pst.setString(3,gender);
          String department=combo_dept.getSelectedItem().toString();
          pst.setString(4,department);
          pst.execute();
         }
        
         else
         {
         
         }
         
       
         
         
         String status="Insert into DoctorStatus(d_id,d_degree,d_salary,d_address,d_mobile) values(?,?,?,?,?)";
          pst=conn.prepareStatement(status);
          
         
          if((idField.getText() != null && !idField.getText().isEmpty())&& (degree_field.getText() != null && !degree_field.getText().isEmpty())
                 &&(salary_field.getText() != null && !salary_field.getText().isEmpty()) && (address_field.getText() != null && !address_field.getText().isEmpty())
                 && (mobileField.getText() != null && !mobileField.getText().isEmpty()))
          
            {
          pst.setString(1,idField.getText());
          pst.setString(2,degree_field.getText());
          pst.setString(3,salary_field.getText());
          pst.setString(4,address_field.getText());
          pst.setString(5,mobileField.getText());
          pst.execute();
          JOptionPane.showMessageDialog(null,"Saved");
          dispose();
            }
             else
            {
                JOptionPane.showMessageDialog(null,"Field Cannot be Empty");
            }
          
      }
      
      catch(Exception e)
      {
      JOptionPane.showMessageDialog(null,e);
      }
    }//GEN-LAST:event_cmd_saveActionPerformed

